package com.zzptc.twds.pojo;

public class AllWorkLoad {
    String year;
    double totalWorkload;
    double courseLoad;
    double scientificLoad;
    double otherWordLoad;

    public String getYear() {
        return year;
    }

    public void setYear(String year) {
        this.year = year;
    }

    public double getTotalWorkload() {
        return totalWorkload;
    }

    public void setTotalWorkload(double totalWorkload) {
        this.totalWorkload = totalWorkload;
    }

    public double getCourseLoad() {
        return courseLoad;
    }

    public void setCourseLoad(double courseLoad) {
        this.courseLoad = courseLoad;
    }

    public double getScientificLoad() {
        return scientificLoad;
    }

    public void setScientificLoad(double scientificLoad) {
        this.scientificLoad = scientificLoad;
    }

    public double getOtherWordLoad() {
        return otherWordLoad;
    }

    public void setOtherWordLoad(double otherWordLoad) {
        this.otherWordLoad = otherWordLoad;
    }


}
